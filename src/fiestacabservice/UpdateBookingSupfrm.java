    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Booking bk=bdao.SearchBookingbyID(bidtxt.getText());
        bk.setassigned(true);
        Vehicle vob=vdao.searchVehiclebyID(Vregtxt.getText());
        vob.setavbty(false);
        vdao.Update(vob);
        bdao.update(bk);
        assignVehicle vaob=new assignVehicle(bidtxt.getText(), Vregtxt.getText());
        boolean status=vadao.insert(vaob);
        if(status==true){
            statustxt.setText("Vehicle Assigned to the Booking");
        }else{
            statustxt.setText("Vehicle Assigning Error");
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed


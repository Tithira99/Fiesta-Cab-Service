    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Vlist1.removeAll();
        try{
            ArrayList<Vehicle> vlist=vdao.searchvhbysub(sbsubtxt.getSelectedItem().toString());

            for(int j=0;j<vlist.size();j++){

               if(vlist.get(j).getavbty()==true) {
                   Vlist1.add("Registration Number :- " + vlist.get(j).getvid());
                   Vlist1.add("Vehice Model :- " + vlist.get(j).getvModel());
                   Vlist1.add("Vehicle Type :- " + vlist.get(j).getvtype());
                   Vlist1.add("Subsidiary :- " + vlist.get(j).getsdiary());
                   Vlist1.add("Availability :- " + vlist.get(j).getavbty());
                   Vlist1.add("Driver ID :- " + vlist.get(j).getdid());
                   Vlist1.add("----------------------------------------" );
               }

            }
        }catch(Exception ex){
            statustxt.setText("Error");
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       Bklist.removeAll();
       try{
       ArrayList<Booking> bkarray=bdao.searchbbysupID(spidtxt.getText());//Searching the bookings by the supervisor ID using a method in Bookig Dao Class
       for(int i=0;i<bkarray.size();i++){
           if((bkarray.get(i).getHended())==false){//checking whhether the booking is ended or not
               //if the booking is not ended , data is added to the awt list
               Bklist.add("Customer Mob.Number :- " +bkarray.get(i).getCmob());//Customer Mobile Number
               Bklist.add("BookingID :- " +bkarray.get(i).getbid());//Booking ID
               Bklist.add("Booking Type :- " +bkarray.get(i).getbtype());//Booking Type
               Bklist.add("Customer Location :- " +bkarray.get(i).getloc());//Location of the Customer
               Bklist.add("Destination :- " +bkarray.get(i).getDes());//Destination 
               Bklist.add("Date placed :- " +bkarray.get(i).getDate1());//Date that the Booking was placed
               Bklist.add("Number of Vehicles Needed :- " +bkarray.get(i).getnVh());//Number of vehicles needed for the Booking
               Bklist.add("Vehicle Type :- " +bkarray.get(i).getvtype());//Vehicle Type
               Bklist.add("Date to Deliver :- " +bkarray.get(i).getDatetoDeliver());//Date to deliver the Vehicle
               Bklist.add("SupervisorID :- " +bkarray.get(i).getsid());//Supervisor to guide the booking
               Bklist.add("Vehicle Assigned :- " +bkarray.get(i).getvasgn());//Vehicle Assigned/not
               Bklist.add("-----------------------------------------------");
           }
       }
       }catch(Exception ex){
          statustxt.setText("Error");
       }
       
       
       
    }//GEN-LAST:event_jButton6ActionPerformed


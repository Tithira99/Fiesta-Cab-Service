    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
       try{
            if(bidtxt.getText().equals("")){
                statustxt.setText(" Booking ID  Should be Entered");
            }else{
                Booking bob = new Booking();
                bob=bdao.SearchBookingbyID(bidtxt.getText());
                bob.setHended(true);
                ArrayList<assignVehicle> avl=vadao.SearchAV(bidtxt.getText());
                for(int i=0;i<avl.size();i++){
                    Vehicle vob=vdao.searchVehiclebyID(avl.get(i).getvid());
                    vob.setavbty(true);
                    vdao.Update(vob);
                    
                }
                bdao.update(bob);
                statustxt.setText("Hire Ended");
            }
        }catch(Exception ex){
           statustxt.setText("Error");
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

